#include "philo.h"

int is_digit(char *arg)
{
	int i;

	i = 0;
	if (arg[i] == '+')
		i++;
	while (arg[i] != '\0')
	{
		if (arg[i] >= '0' && arg[i] <= '9')
			return (1);
		i++;
	}
	return (0);
}